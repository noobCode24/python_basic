# Thao tác với kiểu dữ liệu boolean trong Python

a = True
b = False
print("1. and:", a and b)                # Toán tử AND: True và False = False
print("2. or:", a or b)                  # Toán tử OR: True hoặc False = True
print("3. not:", not a)                  # Toán tử NOT: phủ định True = False
print("4. ==:", a == b)                  # So sánh bằng: False
print("5. !=:", a != b)                  # So sánh không bằng: True

# Lưu ý: Boolean không có phương thức trực tiếp, chỉ dùng toán tử
print("\nLưu ý: Boolean chỉ có giá trị True/False và dùng với toán tử logic.")