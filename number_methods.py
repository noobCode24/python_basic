# Thao tác với kiểu dữ liệu số (int, float, complex) trong Python

# Các hàm thao tác với số (không có phương thức trực tiếp trên đối tượng số)
print("1. abs():", abs(-5))              # Giá trị tuyệt đối của -5: 5
print("2. round():", round(3.14159, 2))  # Làm tròn số 3.14159 với 2 chữ số thập phân: 3.14
print("3. pow():", pow(2, 3))            # Tính lũy thừa 2^3: 8
print("4. int():", int(3.7))             # Chuyển số thực thành số nguyên: 3
print("5. float():", float("3.14"))      # Chuyển chuỗi thành số thực: 3.14

# Toán tử cơ bản
print("6. +:", 5 + 3)                    # Cộng hai số: 8
print("7. **:", 2 ** 3)                  # Lũy thừa 2^3: 8